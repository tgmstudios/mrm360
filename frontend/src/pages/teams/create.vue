<script setup lang="ts">
import { ref, reactive, computed, onMounted, watch } from 'vue'
import { useRouter } from 'vue-router'
import { useTeamStore } from '@/stores/teamStore'
import { useUserStore } from '@/stores/userStore'
import { usePermissions } from '@/composables/usePermissions'
import BaseButton from '@/components/common/BaseButton.vue'
import type { TeamCreate } from '@/types/api'

const router = useRouter()
const teamStore = useTeamStore()
const userStore = useUserStore()
const { can } = usePermissions()

// Check permissions
const canCreate = computed(() => can('create', 'Team'))

// State
const isSubmitting = ref(false)
const selectedUser = ref('')
const selectedRole = ref('MEMBER')
const errors = ref<Record<string, string>>({})

// Form data
const form = reactive({
  name: '',
  type: '',
  subtype: '',
  description: '',
  parentTeamId: '',
  groupId: '',
  members: [] as { userId: string; role: 'MEMBER' | 'LEADER' }[],
  provisioningOptions: {
    authentik: true, // Always enabled
    nextcloudGroup: true, // Always enabled
    wikijs: false,
    nextcloudFolder: false,
    nextcloudCalendar: false,
    nextcloudDeck: false,
    github: false,
    discord: false,
  }
})

// Computed properties
const availableUsers = computed(() => {
  return userStore.users.filter(user => user.isActive)
})

const availableParentTeams = computed(() => {
  return teamStore.teams
})

const isFormValid = computed(() => {
  return form.name && 
         form.type && 
         form.members.length > 0
})

// Watch for team type changes to clear subtype when switching to Development
watch(() => form.type, (newType) => {
  if (newType === 'DEVELOPMENT') {
    form.subtype = ''
  }
})

// Methods
const getUserInitials = (user: any) => {
  if (!user) return 'U'
  return `${user.firstName?.charAt(0)}${user.lastName?.charAt(0)}`.toUpperCase()
}

const getUserById = (userId: string) => {
  return userStore.users.find(u => u.id === userId)
}

const addMember = () => {
  if (!selectedUser.value) return
  
  const existingMember = form.members.find(m => m.userId === selectedUser.value)
  if (existingMember) {
    // If user is already a member, update their role
    existingMember.role = selectedRole.value as 'MEMBER' | 'LEADER'
  } else {
    // Otherwise, add a new member
    form.members.push({ userId: selectedUser.value, role: selectedRole.value as 'MEMBER' | 'LEADER' })
  }
  selectedUser.value = ''
  selectedRole.value = 'MEMBER'
}

const removeMember = (index: number) => {
  form.members.splice(index, 1)
}

const validateForm = () => {
  errors.value = {}

  if (!form.name.trim()) {
    errors.value.name = 'Team name is required'
  }

  if (!form.type) {
    errors.value.type = 'Team type is required'
  }

  if (form.members.length === 0) {
    errors.value.members = 'At least one member is required'
  }

  return Object.keys(errors.value).length === 0
}

const handleSubmit = async () => {
  if (!validateForm()) return
  
  isSubmitting.value = true
  try {
    const createData: TeamCreate = {
      name: form.name,
      type: form.type as 'COMPETITION' | 'DEVELOPMENT',
      subtype: form.subtype as 'BLUE' | 'RED' | 'CTF' | undefined,
      description: form.description,
      parentTeamId: form.parentTeamId || undefined,
      groupId: form.groupId || undefined,
      members: form.members,
      provisioningOptions: form.provisioningOptions,
    }
    
    const newTeam = await teamStore.createTeam(createData)
    router.push(`/teams/${newTeam.id}`)
  } catch (error) {
    console.error('Failed to create team:', error)
  } finally {
    isSubmitting.value = false
  }
}

// Lifecycle
onMounted(async () => {
  await Promise.all([
    userStore.fetchUsers(),
    teamStore.fetchTeams()
  ])
})
</script>
